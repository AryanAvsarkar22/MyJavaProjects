public class Patient {
    public int id;
    public String name;
    public int age;
    public String gender;
    public String phone;
    public String address;

    public Patient(int id, String name, int age, String gender, String phone, String address) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.gender = gender;
        this.phone = phone;
        this.address = address;
    }
}
